    <h2>Social benefits</h2>
      <p>Supporters of ESL programs claim they play an important role in the formation of peer networks and adjustment to school and society in their new homes. Having class among other students learning English as a second language relieves the pressure of making mistakes when speaking in class or to peers. ESL programs also allow students to be among others who appreciate their native language and culture, the expression of which is often not supported or encouraged in mainstream settings. ESL programs also allow students to meet and form friendships with other non-native speakers from different cultures, promoting racial tolerance and multiculturalism.
