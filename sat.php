          <br>There are substantial differences in funding, curricula, grading, and difficulty among U.S. secondary schools due to U.S. federalism, local control, and the prevalence of private, distance, and home schooled students. SAT (and ACT) scores are intended to supplement the secondary school record and help admission officers put local data—such as course work, grades, and class rank—in a national perspective.
        </p>

          <h2>Why is the SAT so important?<br></h2>
          <p>
            <br>The SAT is required by many universities to be considered for admission. In many cases, students can take the ACT rather than the SAT, and this decision often comes down to geography. Students in the Midwest tend to take the ACT and SAT, while students on the U.S. coasts tend to not take the ACT at all. Other than being required for admission to many U.S. universities, the SAT is important for the following reasons:
          </p>
          <ul>
            <br>
            <li>Many scholarships require a minimum SAT score.</li>
            <li>Many universities, especially state universities, will offer academic scholarships for students with a certain GPA and a minimum SAT score.</li>
            <li>The SAT is a great way to set yourself apart from the college admission crowd. Having a high SAT score will allow you to separate out from the thousands of other people applying to college.</li>
          </ul>

      <h2>Details about the test<br></h2>
      <p>
        <br>The SAT consists of three sections: Math, Critical Reading, and Writing. Scores on each section range from 200–800, with scores always being a multiple of 10 (i.e. 580, 720, never 645 or 643). The scores for each of the sections are added together for the composite score, with a 2400 being a "perfect" SAT score.
      </p>
      <p>
        <br>In addition to the nine sections that count towards the student's score, there is a 10th section that will not count but that the College Board administers to test questions for future SATs. The SAT is administered seven times a year in the United States: in October, November, December, January, March (or sometimes April), May, and June. The SAT is usually offered on the first Saturday of the month for November, December, May, and June. Outside of the United States, the SAT is offered on the same dates as in the United States except for the first spring test date, which is either March or April.
      </p>
