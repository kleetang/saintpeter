        <p>
          <br>The SAT is typically taken by high school sophomores, juniors and seniors. The College Board states that SAT measures literacy and writing skills that are needed for academic success in college. They state that the SAT assesses how well the test takers analyze and solve problems—skills they learned in school that they will need in college.
