          <h2>What are the HSE/TASC™ Tests?</h2>
          <p>The TASC™ is a High School Equivalency (HSE) national examination developed by CTB/McGraw Hill. The test is available to adults who did not graduate from high school or whose diplomas may not be recognized by New York State. In New York State, the TASC™ is offered in English and Spanish. There are five tests in the TASC™ Test Battery: Reading; Writing; Social Studies; Science; and Mathematics. The tests take approximately nine hours to complete and include a direct writing assessment.</p>
          <p>Please note: The HSE Office in Albany is the only entity approved to issue high school equivalency diplomas in New York State. If diplomas and/or transcripts are obtained from a source other than the HSE Office, they are not legitimate documents.</p>

          <br><br>
          <h2>Who can take the High School Equivalency Test?</h2>

          <br>
          <p>The New York State Education Department requires all examinees to meet eligibility for HSE/TASC™ Testing in the following areas:</p>

          <br>
          <ul>
            <li>Age Eligibility Requirements</li>
            <p>All 16, 17 or 18 year olds must establish age eligibility each time they wish to test.</p>
          </ul>


          <ul>
            <li>NYS Residency Requirements</li>
            <p>The HSE/TASC™ Tests can be administered to any person who has lived in New York State for at least thirty (30) days prior to the test date AND</p>
          </ul>

          <ul>
            <br>
            <li>Has not graduated from an accredited high school in the United States or its territories; AND,</li>
            <br>
            <li>Has not received a high school equivalency certificate or diploma in the United States or its territories; AND</li>
            <br>
            <li>Is not currently enrolled in a regular high school program of instruction leading to a high school diploma; AND</li>
            <br>
            <li>Has not previously earned scores on the HSE Tests sufficient to qualify for a high school equivalency diploma/certificate.</li>
          </ul>
          


          <br>
          <ul>
            <li>Test Date Eligibility Requirements</li>
            <p>HSE examinees must wait a minimum of sixty (60) days between test sessions when retesting, and examinees may not test more than three (3) times in a calendar year.</p>
          </ul>

